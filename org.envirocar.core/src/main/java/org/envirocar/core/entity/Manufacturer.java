package org.envirocar.core.entity;

public class Manufacturer {
    private String hsn;
    private String name;


    public String getHsn() {
        return hsn;
    }

    public void setHsn(String hsn) {
        this.hsn = hsn;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
