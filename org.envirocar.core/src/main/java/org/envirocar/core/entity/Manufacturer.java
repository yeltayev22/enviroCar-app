package org.envirocar.core.entity;

public class Manufacturer {
    private int hsn;
    private String name;


    public int getHsn() {
        return hsn;
    }

    public void setHsn(int hsn) {
        this.hsn = hsn;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
